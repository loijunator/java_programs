public class Main{
  public static void main(String[] args) {
    System.out.println(Math.PI); //returns pi value
    System.out.println(Math.E); //returns exponential constant
    double result; 
    result = Math.sqrt(9); //returns square root
    result = Math.pow(2, 5); //returns 2^5
    result = Math.abs(-3); //returns distance from 0
    result = Math.round(3.3); //rounds off to nearest integer
    result = Math.ceil(3.1); //rounds off to higher integer                            
    result = Math.floor(3.99); //rounds off to lower integer
    result = Math.max(10, 15); //returns higher number of the two
    result = Math.min(10,20); //returns lower number of the two
    System.out.println(result);                              
  }
}
