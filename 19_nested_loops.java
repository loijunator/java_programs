public class Main{
  public static void main(String[] args) {
    //nested loop -> loop inside another loop
    //used often with matrices or DSA
    //let us print i 3 different times 
    for(int i=1;i<=3;i++){
      for(int j=1; j<=9; j++){
        System.out.print(j + " ");
      }
      System.out.println(" ");
    }
    
  }
}
